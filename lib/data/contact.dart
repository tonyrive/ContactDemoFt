class Contact {
  String FirstName;
  String LastName;
  String FullName;
  String PictureUrl;
  String Picture;
  String Phone;
  String Company;
  String Address;
  String Address2;
  String DisplayName;

  Contact(firstname, lastname, fullname, pictureurl, picture, phone,
      company, address, address2, displayname){
    this.FirstName = firstname;
    this.LastName = lastname;
    this.FullName = fullname;
    this.PictureUrl = pictureurl;
    this.Picture = picture;
    this.Phone = phone;
    this.Company = company;
    this.Address = address;
    this.Address2 = address2;
    this.DisplayName = displayname;
  }
}
